import Foundation

// MARK: - 数据模型

/// 一条成语。第一版仅需字面（4 字），后续可在 JSON 中扩展释义/拼音。
struct ChineseIdiom: Hashable {
    let text: String
    let explanation: String?
    let example: String?
    
    var first: Character { text.first ?? " " }
    var last: Character { text.last ?? " " }
}

// MARK: - 成语库
//
// 加载策略：
// 1. 优先尝试从 Bundle 加载 `idioms.json`（同名也接受 `Datas/Idioms/idioms.json`，
//    Xcode 16 同步文件夹会扁平化到 Bundle 根）。
//    JSON 顶层格式支持以下两种：
//      a) 字符串数组：["一帆风顺", "万紫千红", ...]
//      b) 对象数组（兼容 chinese-xinhua/idiom.json）：[{"word":"一帆风顺", ...}, ...]
//    凡 4 字汉字均收录，自动去重。
// 2. JSON 不存在或解析失败时，回退到内置 fallback 列表（约 800 条精选）。

enum IdiomCatalog {
    /// 全量成语，启动时一次性构建。
    static let all: [ChineseIdiom] = build()

    /// 首字 → 以该字开头的所有成语（保留供未来"接龙"模式用）。
    static let byFirstChar: [Character: [ChineseIdiom]] = {
        var dict: [Character: [ChineseIdiom]] = [:]
        for i in all {
            dict[i.first, default: []].append(i)
        }
        return dict
    }()

    // MARK: - 出题：填空题

    /// 一道填空题。
    struct FillBlankQuestion: Identifiable {
        let id = UUID()
        let idiom: ChineseIdiom
        let blankIndex: Int        // 0..3 挖空位置
        let answerChar: Character  // 正确字
        let options: [Character]   // 6 个候选（含正确，已打乱）
    }

    /// 抽 N 道填空题。
    /// - 候选字干扰来源：从其它成语的字里随机挑，要求与原成语 4 字均不重复，且候选互不重复。
    static func makeFillBlankQuestions(count: Int, optionCount: Int = 6) -> [FillBlankQuestion] {
        guard !all.isEmpty else { return [] }

        // 字符候选池：所有成语的所有字（去重）
        var charPool = Set<Character>()
        for i in all {
            for c in i.text { charPool.insert(c) }
        }
        let charPoolArr = Array(charPool)

        var qs: [FillBlankQuestion] = []
        for idiom in all.shuffled() {
            if qs.count >= count { break }
            let chars = Array(idiom.text)
            guard chars.count == 4 else { continue }
            let blankIndex = Int.random(in: 0..<4)
            let answer = chars[blankIndex]

            // 干扰字：从池中挑选，不与该成语 4 字重复，且互不重复
            let exclude: Set<Character> = Set(chars)
            var distractors: Set<Character> = []
            var safety = 0
            while distractors.count < (optionCount - 1) && safety < 200 {
                if let c = charPoolArr.randomElement(), !exclude.contains(c) {
                    distractors.insert(c)
                }
                safety += 1
            }
            // 如果干扰字仍不足，跳过这题
            guard distractors.count == (optionCount - 1) else { continue }

            var options = Array(distractors)
            options.append(answer)
            options.shuffle()

            qs.append(FillBlankQuestion(
                idiom: idiom,
                blankIndex: blankIndex,
                answerChar: answer,
                options: options
            ))
        }

        // 兜底：题量不足时循环复用
        if qs.count < count, !qs.isEmpty {
            let original = qs
            var i = 0
            while qs.count < count {
                qs.append(original[i % original.count])
                i += 1
            }
        }
        return qs
    }

    // MARK: - 加载

    private static func build() -> [ChineseIdiom] {
        if let loaded = loadFromBundleJSON(), !loaded.isEmpty {
            return loaded
        }
        return dedupAndFilter(fallbackRawList)
    }

    /// 仅保留 4 字 + 全部为汉字的成语，并按 text 去重。
    private static func dedupAndFilter(_ raw: [String]) -> [ChineseIdiom] {
        var seen = Set<String>()
        var result: [ChineseIdiom] = []
        for s in raw {
            guard s.count == 4, s.allSatisfy(isChineseChar) else { continue }
            if seen.insert(s).inserted {
                result.append(ChineseIdiom(text: s, explanation: nil, example: nil))
            }
        }
        return result
    }

    private static func isChineseChar(_ c: Character) -> Bool {
        guard let scalar = c.unicodeScalars.first else { return false }
        return (0x4E00...0x9FFF).contains(scalar.value)
    }

    /// 尝试从 Bundle 加载 idioms.json。
    private static func loadFromBundleJSON() -> [ChineseIdiom]? {
        guard let url = Bundle.main.url(forResource: "idioms", withExtension: "json"),
              let data = try? Data(contentsOf: url) else {
            return nil
        }
        
        struct Entry: Decodable {
            let word: String?
            let explanation: String?
            let example: String?
        }
        
        // 形式 b：对象数组，取 word 字段（兼容 chinese-xinhua/idiom.json）
        if let arr = try? JSONDecoder().decode([Entry].self, from: data) {
            var seen = Set<String>()
            var result: [ChineseIdiom] = []
            for entry in arr {
                guard let word = entry.word, word.count == 4, word.allSatisfy(isChineseChar) else { continue }
                if seen.insert(word).inserted {
                    result.append(ChineseIdiom(text: word, explanation: entry.explanation, example: entry.example))
                }
            }
            return result
        }
        
        // 形式 a：纯字符串数组
        if let arr = try? JSONDecoder().decode([String].self, from: data) {
            return dedupAndFilter(arr)
        }
        
        return nil
    }

